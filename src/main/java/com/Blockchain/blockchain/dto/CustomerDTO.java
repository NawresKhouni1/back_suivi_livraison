package com.Blockchain.blockchain.dto;

public class CustomerDTO {

    private Long id; // Customer ID
    private String name; // Customer name
    private String address; // Customer address
    private String phone; // Customer phone number
    private String email; // Customer email

    // Default constructor
    public CustomerDTO() {}

    // All-args constructor
    public CustomerDTO(Long id, String name, String address, String phone, String email) {
        this.id = id;
        this.name = name;
        this.address = address;
        this.phone = phone;
        this.email = email;
    }

    // Getters and setters
    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }
}

